package de.keithpaterson.veneer;

public class LabelNode {

	private String label;
	
	public LabelNode(String label) {
		this.label = label;
	}

	/**
	 * @param label the label to set
	 */
	public void setLabel(String label) {
		this.label = label;
	}

	public String getLabel() {
		return label;
	}
	
	

}
